package Polymorphism;

 
class Human {
	
	void role() {
		
		System.out.println("I am Human");
		
	}

}



 class Husband extends Human {
	
	void role() {
		
		System.out.println("I am husband");
		
		
	}

}
 
 
 
 class Bro extends Human{
		
		void role() {
			
			System.out.println("I am brother");
			
		}
				
	}
 

 
 