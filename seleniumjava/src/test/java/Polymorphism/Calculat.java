package Polymorphism;

public class Calculat {

	
	
	public static int add(int a, int b , int c) {
		
		return a+b+c;
				
	}
	
	
	public static double add(double x, double y, double z) {
		
		return x+y+z;	
		
	}
	
	
	public static double add(int a, double b) {
		
		return a+b;
				
	}
	
	
	
}
