package Day5;

public class do_while_loop {

	public static void main(String[] args) {
	
		// 2. do while loop
		
		/*		
				Syntax:-
				
				do 
				{
				statements;
				inc/dec;
				
				} while (condition);
				
			*/	
		
		int i=1;
		
		do {
			
			System.out.println(i);
			i++;
		}while (i<=10);

	}

}
